#include<stdio.h>
#include<iostream>
using namespace std;

int main()
{
int t, l, c, i, j, h, w;
    scanf("%d",&t);
    while(t--)
    {
    scanf("%d %d",&l, &c);
    scanf("%d %d",&h, &w);
   
            for(i=0; i<(l*(h + 1)) ; i++)
            {
            for(j=0; j<(c*(w + 1)) ; j++)
                {
                    putchar((i % (h + 1) == 0 || j % (w + 1) == 0) ? '*' : '.');
                }
                putchar('\n');  
            
            }
            if(t > 0){
                putchar('\n');
            }

              
    }
return 0;
}